# Operators that Python Language supports are:
#
# Arithmetic Operators
# Assignment Operators
# Comparison Operators
# Logical Operators
# Identity Operators
# Membership Operators
# Bitwise Operators

# Arithmetic Operators
print("34+9",34+9)
print("54-6",54-6)
print("54/4",54/4)
print("43*7",43*7)
print("49%9",49%9)

# Assignment Operators
x = 5
print(x)
x -=8 # x = x%7
print(x)

# Comparison Operators
x = 5
y = 8
print(x==y)
print(x>=y)
print(x<=y)
print(x!=y)

# Identity Operators
print(1 is not 0)
print(3 is 4)

# Logical Operators
a=4
b=5
print(a<=b)

# Membership Operators
list = [55,54,32,31,67,90]
print(55 not in list)
print(55 in list)

# Bitwise Operators
# 00 00
# 10 00
# 01 00
# 11 10
# 00 11
# 10 11
# 01 01
# 11 01
print(0 & 1)
print(2 | 5)